    private void LlevarCuenta(String Carrera)
    {
        switch(Carrera)
        {
            case "Ingenieria en Sistemas" -> IS++;
            case "Ingenieria Civil" -> IC++;
            case "Psicologia" -> P++;
            case "Derecho" -> D++;
            case "Medicina" -> M++;
            case "Dise√±o Grafico" -> DG++;
            case "Odontologia" -> O++;
            case "Fisioterapia" -> F++;
            case "Filosofia" -> FL++;
            case "Negocios Internacionales" -> NI++;
            case "Administracion" -> AD++;
        }
    }
    
