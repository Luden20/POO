    public void mostrarProducto()
    {
        //Mismo que arriba, selecciono el producto en bse a los combo box
       String Nombre=ProductosCB.getSelectedItem().toString();
       //Muestro todo lo pertienente
       CODIGO_TA.setText(Medicinas.get(Nombre, "CODIGO"));
       PRODUCTO_TA.setText(Medicinas.get(Nombre, "NOMBRE"));
       PRECIO_TA.setText(Medicinas.get(Nombre, "PRECIO"));
       DescripcionTA.setText(Medicinas.get(Nombre, "DESCRIPCION"));
       //Aqui creo un combo box que va desde 1 hasta las existencias , todo se hace una funcion aparte
       CantidadCB.setModel(ModeloCantidad(Integer.parseInt(Medicinas.get(Nombre, "CANTIDAD")))); 
       MarcaTA.setText(Medicinas.get(Nombre,"FABRICANTE"));
       FE.setText(Medicinas.get(Nombre,"FE"));
       FA.setText(Medicinas.get(Nombre,"FA"));
       if(!Medicinas.get(Nombre,"FABRICANTE").equals("xxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxx"))
       {
            String path = Medicinas.get(Nombre,"DI").trim();
            File file=new File(path);
            ImageIcon aux=new ImageIcon(file.getAbsolutePath()); 
            Image xd = aux.getImage().getScaledInstance(350, 350, Image.SCALE_SMOOTH);
            aux=new ImageIcon(xd);
            IMAGEN.setIcon(aux);
       }
       
       mostrarDatos();
    }
