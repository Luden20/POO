    public String Buscar_Posicion( String Cedula) 
	{
            System.out.println(Lista.size());
		for (int i=0; i < Lista.size();i++)
		{
                        System.out.println("Se comparo "+Lista.get(i).getCedula()+" con "+Cedula);
			if (Cedula.equals(Lista.get(i).getCedula()))
			{
                                
				return "Esta en la posicion "+(i+1);
			}
		}
		return "No existe";
	}
	/**Creacion de funcion no miembro que nos permite Buscar un estudiante y retorna el objeto estudiante*/
	public Estudiante Buscar_Estudiante ( String Cedula)
	{
		for (int i=0; i < Lista.size();i++)
		{
			 if (Lista.get(i).getCedula().equals(Cedula))
			{
				return Lista.get(i);
			}	
		}
		return null;
	}

