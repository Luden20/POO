    private void BotonIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonIngresarActionPerformed
        // TODO add your handling code here:
        String NCuenta = TextNCuenta.getText();
        String Nombre = TextNombre.getText();
        String Apellido = TextApellido.getText();
        String CI = TextCICliente.getText();
        double Saldo = Double.parseDouble(TextSaldo.getText());
        Cuenta Ingreso = new Cuenta(NCuenta, Nombre, Apellido, CI, Saldo);
        ListaCuentas.add(Ingreso);
        Clear();
        
    }//GEN-LAST:event_BotonIngresarActionPerformed

    private void BotonBuscarCuentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonBuscarCuentaActionPerformed
        // TODO add your handling code here:
        ParametroBuscar = TextBuscado.getText();
        Cuenta Buscado = BuscarNCuenta(ParametroBuscar);
        if (Buscado != null){
            TextNCuentaB.setText(Buscado.GetNCuenta());
            TextCIClienteB.setText(Buscado.GetCedula());
            TextNombreB.setText(Buscado.GetNombre());
            TextApellidoB.setText(Buscado.GetApellido());
            TextSaldoB.setText(String.valueOf(Buscado.GetSaldo()));
        }
        else{
           TextNCuentaB.setText("Cuenta No Encontrada");
           TextCIClienteB.setText("Cuenta No Encontrada");
           TextNombreB.setText("Cuenta No Encontrada");
           TextApellidoB.setText("Cuenta No Encontrada");
           TextSaldoB.setText("Cuenta No Encontrada"); 
        }
    }//GEN-LAST:event_BotonBuscarCuentaActionPerformed

